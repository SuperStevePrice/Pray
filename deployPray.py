def run(cmd):
    print("+ " + " ".join(cmd))
    return subprocess.run(cmd)


def build_version():
    stamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    try:
        sha = subprocess.run(["git", "rev-parse", "--short", "HEAD"],
                             capture_output=True, text=True, check=True).stdout.strip()
    except Exception:
        sha = ""
    return "Version " + stamp + (" \u00b7 " + sha if sha else "")


def main():
    if not (os.path.isdir("docs") and os.path.isdir(".git")):
        sys.exit("Error: run this from your Pray repo root "
                 "(the folder containing both docs/ and .git).")

    html = base64.b64decode(HTML_B64).decode("utf-8")
    html = (html.replace("__AUTHOR__", AUTHOR)
                .replace("__BG_CREDIT__", BG_CREDIT)
                .replace("__VERSION__", build_version()))

    with open(os.path.join("docs", "index.html"), "w", encoding="utf-8") as f:
        f.write(html)
    print("Wrote docs/index.html (%d chars)." % len(html))

    if run(["git", "add", "docs/index.html"]).returncode != 0:
        sys.exit("git add failed.")

    if subprocess.run(["git", "diff", "--cached", "--quiet"]).returncode == 0:
        print("No changes to commit -- docs/index.html already matches. Done.")
        return

    if run(["git", "commit", "-m", COMMIT_MSG]).returncode != 0:
        sys.exit("git commit failed.")

    if run(["git", "push"]).returncode != 0:
        print()
        print("git push failed. If it failed mysteriously (auth/hang),")
        print("check whether ExpressVPN is running -- it shares the same")
        print("100.64.0.0/10 range as Tailscale and can break things.")
        print("Turn it off, then re-run:  python3 deployPray.py")
        sys.exit(1)

    print()
    print("Pushed. GitHub Pages will rebuild in ~1-2 min.")
    print("Then hard-refresh https://supersteveprice.github.io/Pray/")
    print("Reminder: git pull on your other Mac before working there.")


if __name__ == "__main__":
    main()
